const id = parseInt(params.get("id"));
const project = projects_info.find(p => p.id === id);

if (!project) {
    document.getElementById("project_info").innerHTML = "<h2>Проєкт не знайдено</h2>";
} else {
    document.getElementById("project_title").textContent = project.title;
    document.getElementById("project_description").innerHTML = project.description;

    const gallery = document.getElementById("images_gallery");
    project.images.forEach(imgPath => {
        const img = document.createElement("img");
        img.className = "gallery_img";
        img.src = imgPath;
        img.alt = "Фото проєкту";
        gallery.appendChild(img);
    });
    const topLink = document.getElementById("to_general_link_top");
    const bottomLink = document.getElementById("to_general_link_bottom");

    if (project.categoryPath) {
        if (topLink) topLink.href = project.categoryPath;
        if (bottomLink) bottomLink.href = project.categoryPath;
    }
}
